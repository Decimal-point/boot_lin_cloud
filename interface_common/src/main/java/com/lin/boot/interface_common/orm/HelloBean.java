package com.lin.boot.interface_common.orm;

public class HelloBean {
    private Integer id;
    private String name;
    private Integer UserBody;


    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getUserBody() {
        return UserBody;
    }

    public void setUserBody(Integer userBody) {
        UserBody = userBody;
    }
}
